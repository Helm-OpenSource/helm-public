#!/usr/bin/env tsx
/**
 * Public-mirror extensions stub builder.
 *
 * Per audit §3.3 / fix log §15.6: `lib/extensions/registry.tsx` statically
 * imports tenant-specific modules from `@/extensions/<tenant>/*`. The current
 * private repo includes those modules and the imports resolve. The public
 * mirror does NOT include them — the comment at the top of registry.tsx
 * documents that the mirror generator must either replace registry.tsx with
 * a stub or remove tenant entries from the local arrays.
 *
 * This script implements the "replace with a stub" path. It is deterministic
 * and idempotent: running it twice produces the same output. Mirror CI can
 * invoke it before pushing.
 *
 * Usage:
 *   npx tsx scripts/build-public-mirror-extensions-stub.ts
 *   npx tsx scripts/build-public-mirror-extensions-stub.ts --check   # exit 1 if registry would change
 *
 * The stub keeps every public function signature exported by the original
 * registry so downstream call sites compile unchanged. Each function returns
 * the empty / null shape that means "no extension is available" — the same
 * shape the original returns when `getAccess` resolves to `{ ok: false }`.
 */

