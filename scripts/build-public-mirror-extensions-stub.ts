export function resolveRegistryPath(repoRoot = process.cwd()): string {
  return path.resolve(repoRoot, "lib/extensions/registry.tsx");
}

export function buildPublicMirrorExtensionsStub(
  options: PublicMirrorExtensionsStubOptions = {},
): PublicMirrorExtensionsStubResult {
  const registryPath = resolveRegistryPath(options.repoRoot);
  let existing = "";
  try {
    existing = readFileSync(registryPath, "utf8");
  } catch (error) {
    throw new Error(`failed to read ${registryPath}: ${(error as Error).message}`);
  }

  if (existing === STUB_CONTENT) {
    return {
      status: "already-stub",
      registryPath,
      exitCode: 0,
    };
  }

  if (options.checkMode) {
    return {
      status: "not-stub",
      registryPath,
      exitCode: 1,
    };
  }

  writeFileSync(registryPath, STUB_CONTENT, "utf8");
  return {
    status: "wrote-stub",
    registryPath,
    exitCode: 0,
  };
}

function main() {
  const args = process.argv.slice(2);
  const checkMode = args.includes("--check");

  let result: PublicMirrorExtensionsStubResult;
  try {
    result = buildPublicMirrorExtensionsStub({ checkMode });
  } catch (error) {
    console.error((error as Error).message);
    process.exit(2);
  }

  if (result.status === "not-stub") {
    console.error(
      `not-stub: ${result.registryPath} differs from stub — run \`npx tsx scripts/build-public-mirror-extensions-stub.ts\` before pushing the public mirror.`,
    );
    process.exit(result.exitCode);
  }

  console.log(`${result.status}: ${result.registryPath}`);
  process.exit(result.exitCode);
}

if (require.main === module) {
  main();
}
